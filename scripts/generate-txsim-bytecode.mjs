import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const artifactPath = resolve('out/TxSimulator.sol/TxSimulator.json');
const outPath = resolve('src/generated/txSimulatorBytecode.ts');
const artifact = JSON.parse(readFileSync(artifactPath, 'utf8'));
const object = artifact.deployedBytecode?.object;

if (typeof object !== 'string' || object.length === 0) {
  throw new Error(`Missing deployed bytecode in ${artifactPath}`);
}

const bytecode = object.startsWith('0x') ? object : `0x${object}`;
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(
  outPath,
  [
    'import type { Hex } from \'viem\';',
    '',
    '// Generated by scripts/generate-txsim-bytecode.mjs.',
    `export const txSimulatorRuntimeBytecode = '${bytecode}' as const satisfies Hex;`,
    '',
  ].join('\n'),
);
